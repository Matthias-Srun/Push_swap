#include "ft_printf.h"

int		ft_putnbr(long long n)
{
	int i;

	i = 0;
	if (n < 0)
	{
		n = -n;
		ft_putchar('-');
		i++;
	}
	i += ft_nbrlen(n);
	if (n / 10)
		ft_putnbr(n / 10);
	ft_putchar(n % 10 + 48);
	return (i);
}
